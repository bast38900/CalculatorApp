export const add = (a: number, b: number) => {
  const result = a + b;
  return result;
};

export const subtract = (a: number, b: number) => {
  const result = a - b;
  return result;
}

export const multiply = (a: number, b: number) => {
  const result = a * b;
  return result;
}

export const divide = (a: number, b: number) => {
  const result = a / b;
  return result;
}